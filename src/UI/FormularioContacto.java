    private void btnAceptarContactoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarContactoActionPerformed
// Validación básica: Nombre, Apellido y Teléfono son obligatorios
        if (txtNombre.getText().trim().isEmpty() ||
            txtApellidos.getText().trim().isEmpty() ||
            txtTelefono.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nombre, Apellido y Teléfono son campos obligatorios.", "Error de Validación", JOptionPane.WARNING_MESSAGE);
            return;
        }
        // Asigna los valores de los campos de texto a la instancia de Contacto
        contacto.setNombres(txtNombre.getText().trim());
        contacto.setApellidos(txtApellidos.getText().trim());
        contacto.setTelefono(txtTelefono.getText().trim());
        contacto.setEmail(txtEmail.getText().trim());

        dispose(); // Cierra el JDialog
    }//GEN-LAST:event_btnAceptarContactoActionPerformed

