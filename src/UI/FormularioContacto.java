    /**
     * Constructor para editar un contacto existente.
     * @param parent El frame padre.
     * @param modal Si el diálogo debe ser modal.
     * @param contactoAEditar El objeto Contacto a editar.
     */
    public FormularioContacto(java.awt.Frame parent, boolean modal, Contacto contactoAEditar) {
        super(parent, modal);
        initComponents();
        setLocationRelativeTo(parent); // Centra el formulario respecto a la ventana principal
        this.esEdicion = true; // Modo edición
        this.contacto = contactoAEditar; // Se referencia el contacto existente
        cargarDatosContacto(); // Carga los datos del contacto en los campos del formulario
        setTitle("Editar Contacto"); // Título del diálogo
    }

    private void cargarDatosContacto() {
        if (contacto != null) {
            txtNombre.setText(contacto.getNombres());
            txtApellidos.setText(contacto.getApellidos());
            txtTelefono.setText(contacto.getTelefono());
            txtEmail.setText(contacto.getEmail());
        }
    }
    public Contacto getContacto() { // ¡Importante: este método debe ser público!
    return contacto;
}
